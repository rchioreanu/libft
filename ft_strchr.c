#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;
	char	*d;

	d = (char *)s;
	i = 0;
	while (d[i])
	{
		if (d[i] == c)
			return (d + i);
		i++;
	}
	if (c == '\0')
		return (d + i);
	return (0);
}
